def aircraft_long_name(type_code: str | None) -> str | None:
    """Return the friendly name for an ICAO type designator.

    Falls back to the raw code (uppercased) when the designator isn't in
    the table — so a never-before-seen aircraft still shows *something*.
    Returns ``None`` when the input is empty or just whitespace.
    """
    if not type_code:
        return None
    code = type_code.strip().upper()
    if not code:
        return None
    name = _AIRCRAFT_NAMES.get(code)
    if name:
        return name
    return code
